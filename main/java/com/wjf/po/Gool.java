package com.wjf.po;

public class Gool {
    private Integer goolId;
    private String goolName;
    private String goolType;
    private String goolWeight;

    private Integer pageSize;
    private Integer pageIndex;

    public Integer getGoolId() {
        return goolId;
    }

    public void setGoolId(Integer goolId) {
        this.goolId = goolId;
    }

    public String getGoolName() {
        return goolName;
    }

    public void setGoolName(String goolName) {
        this.goolName = goolName;
    }

    public String getGoolType() {
        return goolType;
    }

    public void setGoolType(String goolType) {
        this.goolType = goolType;
    }

    public String getGoolWeight() {
        return goolWeight;
    }

    public void setGoolWeight(String goolWeight) {
        this.goolWeight = goolWeight;
    }

    public Integer getPageSize() {
        return pageSize;
    }

    public void setPageSize(Integer pageSize) {
        this.pageSize = pageSize;
    }

    public Integer getPageIndex() {
        return pageIndex;
    }

    public void setPageIndex(Integer pageIndex) {
        this.pageIndex = pageIndex;
    }
}
